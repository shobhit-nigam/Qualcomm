#lists indexing & slicing

living = ["bat", "cat", "dog", "eat"]
num = [5, 7, 9, 10, 11, 13, 19]

print(type(living))

print(type(num))

print("living=",living)
print("living[0]=", living[0])
print("living[-1]=", living[-1])
print("num[2:10]=", num[2:10])

print("num[:4]=", num[:4])
print("num[3:]=", num[3:])
print("num[:]=", num[:])
print("num[:]=", num[0:6:2])
